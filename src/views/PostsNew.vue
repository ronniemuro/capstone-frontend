<script>
import axios from "axios";
export default {
  data: function () {
    return {
      newPostParams: {},
      errors: [],
    };
  },
  created: function () {},
  methods: {
    submit: function () {
      axios
        .post("/posts.json", this.newPostParams)
        .then((response) => {
          console.log(response.data);
          this.$router.push("/posts");
        })
        .catch((error) => {
          this.errors = error.response.data.errors;
        });
    },
  },
};
</script>

<template>
  <div class="new-post">
    <form v-on:submit.prevent="submit()">
      <h2>New Post</h2>
      <ul>
        <li v-for="error in errors" v-bind:key="error">{{ error }}</li>
      </ul>
      <div>
        <label>How are the planets aligning for you?</label>
        <input type="text" v-model="newPostParams.post_content" />
      </div>
      <div>
        <label>Sign Type:</label>
        <select name="Sign" v-model="newPostParams.sign_type">
          <option value="Sun">Sun</option>
          <option value="Moon">Moon</option>
          <option value="Rising">Rising</option>
        </select>
      </div>
      <div>
        <label>Sign:</label>
        <select name="Sign" v-model="newPostParams.sign">
          <option value="Aries">Aries</option>
          <option value="Taurus">Taurus</option>
          <option value="Gemini">Gemini</option>
          <option value="Cancer">Cancer</option>
          <option value="Leo">Leo</option>
          <option value="Virgo">Virgo</option>
          <option value="Libra">Libra</option>
          <option value="Scorpio">Scorpio</option>
          <option value="Sagittarius">Sagittarius</option>
          <option value="Capricorn">Capricorn</option>
          <option value="Aquarius">Aquarius</option>
          <option value="Pisces">Pisces</option>
        </select>
      </div>
      <input type="submit" value="Create" />
    </form>
  </div>
</template>

